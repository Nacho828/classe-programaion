# Ejemplo 1: Iterar sobre una lista de números
numeros = [1, 2, 3, 4, 5]
for e in numeros:
    print(f"Número: {e}")

# Ejemplo 2: Iterar sobre un rango de números
for i in range(7):
    print(f"Iteración: {i}")

# Ejemplo 3: Iterar sobre una cadena de texto
texto = "Hola"
for letra in texto:
    print(f"Letra: {letra}")